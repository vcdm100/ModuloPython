#Exemplos de Operadores Relacionais

x = 6;
y = 8;

#Maior que:
print(x > y);
print();

""" RESULTADO (TERMINAL)

False

"""

#Menor que:
print(x < y);
print();

""" RESULTADO (TERMINAL)

True

"""

#Igualdade:
print(x == y, "\n");

""" RESULTADO (TERMINAL)

False
 
"""

#Maior ou igual a:
print(x >= y, "\n");

""" RESULTADO (TERMINAL)

False
 
"""

#Menor ou igual a:
print(x <= y, "\n");

""" RESULTADO (TERMINAL)

True
 
"""

#Diferente:
print(x != y, "\n");

""" RESULTADO (TERMINAL)

True
 
"""
