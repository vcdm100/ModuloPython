#Exemplos de Operadores Aritméticos

a = 15;
b = 6;

soma            = a + b;
subtracao       = a - b;
multiplicacao   = a * b;
divisao         = a / b;
resto           = a % b;
potencia        = a ** b;
restoDescartado = a // b;

print("Soma: ", soma);
print("Subtração: ", subtracao);
print("Multiplicação: ", multiplicacao);
print("Divisão: ", divisao);
print("Potência: ", potencia);
print("Resto da divisão: ", resto);
print("Divisão após descartartar a parte fracionária: ", restoDescartado);
print();

